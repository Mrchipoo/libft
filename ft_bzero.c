#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t				i;
	unsigned char		*u1;

	i = 0;
	u1 = (unsigned char *)s;
	while (i < n)
	{
		u1[i] = 0;
		i++;
	}
}
